$(document).ready(async function () {
  const tokenContract = '0xd7d58712fe1bd6ffef8b518d4d28923e419da525';
  const custodian = '0xd1C7BD89165f4c82e95720574e327fa2248F9cf2';
  // Connect web3
  const connectBtn = $('.web3-connect');
  const transferBtn = $('.transfer');
  // Connect provider & signer
  const provider = new ethers.providers.Web3Provider(web3.currentProvider);
  const signer = provider.getSigner();
  // Start checking for metamask
  if (typeof window.ethereum !== 'undefined') {
    try {
      const accounts = await ethereum.request({method: 'eth_requestAccounts'});
      const account = accounts[0];
      if (account) {
        updateTextOf(connectBtn, account);
      }
    } catch (error) {
      console.log(error);
    }
  }

  // Handle Transfer
  connectBtn.on('click', async function () {
    const accounts = await ethereum.request({method: 'eth_requestAccounts'});
    const account = accounts[0];
    updateTextOf(connectBtn, account);
    return false;
  });

  ethereum.on('accountsChanged', function (accounts) {
    updateTextOf(connectBtn, accounts[0]);
  });

  // Handle transfer
  transferBtn.on('click', async function () {
    const value = $('.transfer-value').val();

    if (!value) {
      return console.log('Value Invalid');
    }

    const Contract = new ethers.Contract(tokenContract, abi, signer);
    const receipt = await Contract.transfer(
      custodian,
      ethers.utils.parseUnits(value, 6),
    );

    console.log(receipt);
    $('.transaction-history').append(
      `<a href='https://goerli.etherscan.io/tx/${receipt.hash}' target="_blank">${receipt.hash}</a>`,
    );
  });

  // Handle Signer
  $('.signer').on('click', async function () {
    const address = $('.xpx-address').val();
    const transactionHash = $('.transaction-hash').val();
    console.log(address);
    console.log(transactionHash);
    const originMessage = `${transactionHash}|${address}`;
    const messageSignature = await signer.signMessage(originMessage);
    const data = {
      address: ethereum.selectedAddress,
      originMessage,
      messageSignature,
    };

    document.getElementById('signature-data').textContent = JSON.stringify(
      data,
      undefined,
      2,
    );
  });

  // Handle Claim XPX
  $('.claim-xpx').on('click', async function () {
    $('.claim-xpx').prop('disabled', true);
    const signatureData = $('.prove-signature').val();

    const response = await fetch('/verify-message', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: signatureData, // body data type must match "Content-Type" header
    });

    const responseData = await response.json();

    alert(JSON.stringify(responseData));
    $('.claim-xpx').prop('disabled', false);
  });

  function updateTextOf(element, text) {
    return element.text(text);
  }
});
